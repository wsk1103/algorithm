    public static int guessNumber(int n) {
        int start = 1;
        int end = n;
        while (start <= end) {
            int mid = (end - start) / 2 + start;
            int gg = guess(mid);
            if (gg == 0) {
                return mid;
            } else if (gg > 0) {
                start = mid + 1;
            } else {
                end = mid - 1;
            }
        }
        return -1;
    }

    static int p;

    static int guess(int num) {
        return Integer.compare(p, num);
    }

