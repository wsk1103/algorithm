    public static int handle(int n) {
        System.err.println(n);
        int to = 0;
        int first = Integer.MAX_VALUE - 100;
        int loop = 0;
        while (n > 0) {
            if ((n & 1) == 1) {
                to = Math.max(loop - first, to);
                first = loop;
            }
            loop++;
            n = n >> 1;
        }
        return to;
    }

