        System.out.println(JSONUtil.toJsonStr(nums));
        int len = nums.length;
        if (len <= 0) {
            return;
        }
        for (int i = len - 1; i > 0; i--) {
            if (nums[i] > nums[i - 1]) {
                Arrays.sort(nums, i, len);
                for (int j = i; j < len; j++) {
                    if (nums[j] > nums[i - 1]) {
                        int temp = nums[j];
                        nums[j] = nums[i - 1];
                        nums[i - 1] = temp;
                        System.err.println(JSONUtil.toJsonStr(nums));
                        return;
                    }
                }
            }

        }
        Arrays.sort(nums);
