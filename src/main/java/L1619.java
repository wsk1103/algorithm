    public static double handle(int[] arr) {
        System.err.println(JSONUtil.toJsonStr(arr));
        int len = arr.length;
        int del = (int) (len * 0.05);
        Arrays.sort(arr);
        for (int i = 0; i < del; i++) {
            arr[i] = 0;
        }
        for (int i = len - 1; i >= len - del; i--) {
            arr[i] = 0;
        }
        double sum = 0;
        for (int num : arr) {
            sum += num;
        }
        return sum / (len - 2 * del);
    }

