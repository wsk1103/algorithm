    public static int handle(int[] nums) {
        int len = nums.length;
        int[] dp = new int[len];
        int res = 1;
        for (int i = 0; i < len; i++) {
            dp[i] = 1;
            for (int j = 0; j < i; j++) {
                if (nums[i] > nums[j]) {
                    dp[i] = Math.max(dp[i], dp[j] + 1);
                }
            }
            res = Math.max(dp[i], res);
        }
        return res;
    }

    public static void main(String[] args) {
        int[] nums;
        nums = new int[]{4, 10, 4, 3, 8, 9};
        System.err.println(handle(nums));
        nums = new int[]{10, 9, 2, 5, 3, 7, 101, 18};
        System.err.println(handle(nums));
    }

